import { pget, ppost } from '../../../../utils/request'



/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
加载课前思考描述
  -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
export function loadDescription(problemId,chapter,section){
  console.log('进入get请求')
  return pget(`/pc/operation/preview/load/description/${problemId}?chapter=${chapter}&section=${section}`)
}

/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
更新课前思考
  -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
export function updatePreview(problemId,param){
  return ppost(`/pc/operation/preview/update/${problemId}`,param)
}
